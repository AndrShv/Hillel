package DarthAnimOld;

public class Point {
    private final int x;
    private final int y;
    private final int h;
    private final int w;

    public Point(int x, int y, int h, int w) {
        this.x = x;
        this.y = y;
        this.h = h;
        this.w = w;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getH() {
        return h;
    }

    public int getW() {
        return w;
    }
}
